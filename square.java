import java.lang.Math;

class square {
    public static void main(String[] args) {
        int X = 3;
        int S = X * X;
        double R = Math.sqrt(X);
        System.out.println("The square of " + X + " is " + S);
        System.out.println("The square root of " + X + " is " + R);
    }
}